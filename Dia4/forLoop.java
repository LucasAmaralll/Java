package Dia4;

public class forLoop {
    public static void main(String[] args) {
        // Inicialização da variável de controle (i) com valor 1
        // A condição do loop: continuar enquanto i for menor ou igual a 10
        // Incremento: aumentar o valor de i em 1 a cada iteração
        for (int i = 1; i <= 10; i++) {
            // Imprime o valor atual de i seguido por um espaço
            System.out.print(i + " ");
        }
        // O loop termina quando i não atende mais à condição (i <= 10)
    }
}
