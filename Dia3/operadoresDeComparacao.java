package Dia3;

// Classe chamada "operadoresDeComparacao"
public class operadoresDeComparacao {
    
    // Método principal, onde a execução do programa começa
    public static void main(String[] args) {
        
        // Declaração e inicialização de variáveis inteiras "a" e "b"
        int a = 4;
        int b = 5;
        
        // Verifica se "a" é igual a "b" e imprime o resultado (resultado booleano)
        System.out.println(a == b);
        
        // Verifica se "a" é diferente de "b" e imprime o resultado (resultado booleano)
        System.out.println(a != b);
        
        // Verifica se "a" é maior que "b" e imprime o resultado (resultado booleano)
        System.out.println(a > b);
        
        // Verifica se "a" é menor que "b" e imprime o resultado (resultado booleano)
        System.out.println(a < b);
    }
}
