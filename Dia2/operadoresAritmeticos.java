package Dia2;

// Classe chamada "operadoresAritmeticos"
public class operadoresAritmeticos {
    
    // Método principal, onde a execução do programa começa
    public static void main(String[] args) {
        
        // Expressão aritmética: (6 + 2) * 2
        int a = (6 + 2) * 2;
        
        // Expressão aritmética: 6 + 2 * (2 - 3)
        int b = 6 + 2 * (2 - 3);
        
        // Imprime o resultado da primeira expressão aritmética
        System.out.println(a);
        
        // Imprime o resultado da segunda expressão aritmética
        System.out.println(b);
    }
}
